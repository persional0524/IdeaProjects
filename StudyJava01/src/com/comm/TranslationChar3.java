package com.comm;

public class TranslationChar3 {
    public static void main(String[] args) {
        System.out.println("王爱码字爱java");
        //"\t"水平制表符 tab键
        System.out.println("王爱码字爱\tjava");
        System.out.println("王爱码字爱\t\t\t\t\t\t\t\tjava");
        System.out.println("王爱码字    爱java");

        //"\n"换行符 同一种效果可以好多种表达方式
        System.out.println("刘涛爱java");
        System.out.println("----------");
        System.out.print("刘涛爱java");
        System.out.println();
        System.out.print("刘涛\n爱java");
        System.out.print("\n刘\n涛\n爱\njava");

        //console 控制台
        //数据：视频 图片 音频 文本
    }
}
